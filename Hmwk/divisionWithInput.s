/*DIVISION WITH INPUT AND OUTPUT*/

	.GLOBAL _MAIN
_MAIN:
_WRITE:
					@ WRITE SYSCALL
	MOV R7, #4		@ SYSCALL NUMBER
	MOV R0, #1		@ STDOUT IS MONITOR
	MOV R2, #48		@ STRING IS 48 CHARS LONG
	LDR R1,=STRING	@ STRING LOCATED AT STRING:
	SWI 0

_READ:				@ READ SYSCALL
	MOV R7, #3		@ SYSCALL NUMBER
	MOV R0, #0		@ STDOUT IS MONITOR
	MOV R2, #5		@ READ FIRST 5 CHARACTERS
	LDR R2,=STRING2 @ STRING PLACED AT STRING2
	SWI 0

	MOV R7, #3		@ SYSCALL NUMBER
	MOV R0, #0		@ STDOUT IS MONITOR
	MOV R2, #5		@ READ FIRST 5 CHARACTERS
	LDR R3,=STRING3 @ STRING PLACED AT STRING3
	SWI 0

_ASSIGNRGSTR:
	LDR R2, [R2]	@ PLACE NUMERATOR INTO REGISTER 2
	LDR R3, [R3]	@ PALCE DENOMINATOR INTO REGISTER 3
	MOV R4, #1
	MOV R5, R3
	MOV R0, #0
	MOV R1, R2 
	CMP R1, R3
	BLT _EXIT
	BGE _SCALELEFT
	
_SCALELEFT:
	MOV R4, R4, LSL #1
	MOV R5, R5, LSL #1
	CMP R1, R5
	BGT _SCALELEFT
	MOV R4, R4, LSR #1
	MOV R5, R5, LSR #1
	
_ADDSUB:
	ADD R0, R0, R4
	SUB R1, R1, R5
	B _SCALERIGHT
	CMP R4, #1
	BGT _ADDSUB
	
_SCALERIGHT:
	MOV R4, R4, LSR #1
	MOV R5, R5, LSR #1
	CMP R1, R5
	BLT _SCALERIGHT

_EXIT:
					@ EXIT SYSCALL
	MOV R7, #1 
	SWI 0

.DATA
STRING:
.ASCII "Type the numerator followed by the denominator:\n"
STRING2:
.ASCII "    \n"
STRING3:
.ASCII "    \n"