.GLOBAL _START

	_START:
		MOV R0, #0			@ DIVISION COUNTER
		MOV R1, R2			@ REMAINDER AFTER DIVISION
		MOV R2, #111		@ VARIABLE TO BE DIVIDED
		MOV R3, #5			@ VARIABLE TO DIVIDE R2 BY	
		MOV R4, #0			@ UNUSED VARIABLE????
		MOV R5, #0			@ EMPTY VARIABLE TO SWAP
		CMP R1, R3
		BLT _SWAP
		BEQ _END
	
	_SUBTRACT:
		ADD R0, R0, #1
		SUB R1, R1, R3
		CMP R1, R3
		BGE _SUBTRACT
		
	_SWAP:
		MOV R5, R0
		MOV R0, R1
		MOV R1, R5
		
	_END:
		MOV R7, #1
		SWI 0